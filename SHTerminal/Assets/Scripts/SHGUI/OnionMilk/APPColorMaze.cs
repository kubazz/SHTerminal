	public override void Update()
	{
		base.Update();

		if(inStory) //historyjka
		{
			timerStory -= Time.unscaledDeltaTime;
			if(timerStory <= 0.0f)
			{
				++sceneStory;

				if(sceneStory == 1) timerStory = 0.8f; //it
				else if(sceneStory == 2) timerStory = 0.8f; //is
				else if(sceneStory == 3) timerStory = 2.3f; //you
				else if(sceneStory == 4) timerStory = 0.8f; //you
				else if(sceneStory == 5) timerStory = 0.8f; //are
				else if(sceneStory == 6) timerStory = 0.8f; //have to
				else if(sceneStory == 7) timerStory = 2.3f; //found
				else if(sceneStory == 8) timerStory = 1.5f; //this
				else if(sceneStory == 9) timerStory = 0.8f; //this
				else if(sceneStory == 10) timerStory = 0.8f; //is
				else if(sceneStory == 11) timerStory = 2.3f; //exit
				else if(sceneStory == 12)
				{
					inStory = false;
				}
			}
		}
		else if(!inMenu) //rozgrywka
		{
			if(lvl < 11)
			{
				if(startPosX == posX && startPosY == posY || lvl == 11) APPINSTRUCTION.text = "ESC-to-quit";
				else APPINSTRUCTION.text = "ESC-to-reset";

				if(lvl == 0) APPINSTRUCTION.text = "ESC-to-quit";

				if(map[posX, posY] == CellState.Portal)
				{
					if(kolor == 0)
					{
						map[posX, posY] = CellState.Black;
						kolor = 1;
					}
					else if(kolor == 1)
					{
						map[posX, posY] = CellState.White;
						kolor = 0;
					}
				}
				if(map[posX, posY] == CellState.End)
				{
					loadLvl(lvl + 1);
				}

				gameTimer += Time.unscaledDeltaTime;
			}
			else //zakończenie gry
			{
				nextDotTime -= Time.unscaledDeltaTime;
				if(nextDotTime <= 0f)
				{
					++dotCount;
					if(dotCount < 4) 
					{
						endGameText = endGameText + ".";

					}
					else
					{
						endGameText = "HACK COMPLETE";
						dotCount = 0;
					}

					nextDotTime = 0.5f;
				}
			}
		}
		else //menu
		{
			timeHintText -= Time.unscaledDeltaTime;

			if(timeHintText <= 0f)
			{
				if(onHintText)
				{
					onHintText = false;
					timeHintText = 0.3f;
				}
				else
				{
					onHintText = true;
					timeHintText = 0.6f;
				}
			}
		}
	}

